/**** DECLARING AND INITIALIZING VARIABLES: PRACTICE SOLUTION ****/

/*
    Time to practice declaring and initializing variables! 
    
    Remember:
        - Always use the 'let' keyword when declaring
        - Don't use the 'let' keyword if initializing later on a separate line.
        - Give your variable a meaningful name using the camelCase convention.
        - Use the assignment operator = to initialize with a value.
        - Use single or double quotes for strings (collections of characters).
        - Do not use quotes for numbers like 24, 3.7, or -85.
*/

// 1. Create a variable called myFavoriteMovie that stores the name
// of one of your favorite movies, assign a value, then print the
// variable to the console to see its stored value.
let myFavoriteMovie = "House of Flying Daggers";
console.log(myFavoriteMovie);

// 2. Create a variable called futureVacationSpot that stores a location
// you plan to visit one day, assign a value, then print the variable to the
// console to see its stored value.
let futureVacationSpot = "Iceland";
console.log(futureVacationSpot);

// 3. Create a variable called perfectTemp that stores your ideal temperature
// outside on a 'perfect day', assign a value, then print the variable
// to the console to see its stored value.
let perfectTemp = 72;
console.log(perfectTemp);
// Also okay if you initialized to a string of characters, like "72°F"

// 4A. Declare a variable called numberOfStatesVisited, but do not initialize it yet.
let numberOfStatesVisited;

// 4B. Think about how many states in the U.S. you've been to. For some of us
// this could be a pretty big number that requires a lot of thought! Here's a tool you can use:
